#include "mylib.h"

using namespace MyLib;

//--------------------------
// Vector3
//--------------------------
// //==========================================================================
// コンストラクタ
//==========================================================================
Vector3::Vector3()
{
	x = 0.0f;
	y = 0.0f;
	z = 0.0f;
}

//==========================================================================
// コンストラクタ
//==========================================================================
Vector3::Vector3(const float xyz)
{
	x = xyz;
	y = xyz;
	z = xyz;
}

//==========================================================================
// 加算のオーバーロード
//==========================================================================
Vector3 Vector3::operator + (const float& o)
{
	return Vector3(x + o, y + o, z + o);
}

//==========================================================================
// 加算代入のオーバーロード
//==========================================================================
Vector3& Vector3::operator += (const float& o)
{
	x += o;
	y += o;
	z += o;
	return *this;
}
